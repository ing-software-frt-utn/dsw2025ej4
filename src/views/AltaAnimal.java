    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        String animal = listaAnimal.getSelectedItem().toString();
        int peso = Integer.parseInt(pesoText.getText());
        int edad = Integer.parseInt(edadText.getText());
        int codigo = Integer.parseInt(codigoText.getText());
        String pais = paisText.getText();
        
        try {
            pesoText.setText("");
            edadText.setText("");
            codigoText.setText("");
            paisText.setText("");
            Persistencia.inicializar(animal,pais,codigo,edad,peso);
        } catch (InvalidPropertiesFormatException ex) {
           
            Logger.getLogger(AltaAnimal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

