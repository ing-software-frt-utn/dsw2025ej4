    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        ArrayList<Especie> especies = Controlador.getEspecies();
        String nombreEspecie = (String) jComboBoxEspecie.getSelectedItem();
        String pais = (String) jComboBoxPais.getSelectedItem();
        String sector = (String) jComboBoxSector.getSelectedItem(); 
        int numeroSector = Integer.parseInt(sector);
        
        if(jTextField3.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "El campo peso no puede estar vacio");
            return;
        }
        double peso = Double.parseDouble(jTextField3.getText());
        
        String edadTexto = jTextField2.getText();
        if(edadTexto.isEmpty()){
            JOptionPane.showMessageDialog(this, "El campo edad no puede estar vacio");
            return;
        }
        int edad = Integer.parseInt(edadTexto);
                
        for(Especie especie : especies){
            if(jComboBoxEspecie.getSelectedItem().equals(especie.getNombre()) && especie.getTipoAlimentacion().esCarnivoro()) {
                try {
                    Controlador.agregarCarnivoro(nombreEspecie, pais, numeroSector, peso, edad);
                    jLabel4.setVisible(true);

                    // Timer para ocultar el JLabel después de 2 segundos (2000 milisegundos)
                    javax.swing.Timer timer = new javax.swing.Timer(2000, new java.awt.event.ActionListener() {
                        @Override
                        public void actionPerformed(java.awt.event.ActionEvent e) {
                            jLabel4.setVisible(false);
                            limpiar();
                        }
                    });
                    timer.setRepeats(false); // Para que se ejecute solo una vez
                    timer.start();

                } catch (InvalidPropertiesFormatException e) {
                    e.printStackTrace();
                }
            } else if (jComboBoxEspecie.getSelectedItem().equals(especie.getNombre()) && especie.getTipoAlimentacion().esHerbivoro()) {
                String extraAlimentoTexto = (String) jTextField4.getText();
                if (extraAlimentoTexto.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "El campo Alimento Extra no puede estar Vacío");
                    return;
                    
                }
                double extraAlimento = Double.parseDouble(extraAlimentoTexto);
                
                try {
                    Controlador.agregarHerbivoro(numeroSector, peso, nombreEspecie, edad, extraAlimento, pais);
                    jLabel4.setVisible(true);

                    // Timer para ocultar el JLabel después de 2 segundos (2000 milisegundos)
                    javax.swing.Timer timer = new javax.swing.Timer(2000, new java.awt.event.ActionListener() {
                        @Override
                        public void actionPerformed(java.awt.event.ActionEvent e) {
                            jLabel4.setVisible(false);
                            limpiar();
                        }
                    });
                    timer.setRepeats(false); // Para que se ejecute solo una vez
                    timer.start();

                } catch (InvalidPropertiesFormatException e) {
                    e.printStackTrace();
                }
//                
//                try {
//                    Controlador.agregarHerbivoro(numeroSector, peso, nombreEspecie, edad, extraAlimento, pais);
//                } catch (InvalidPropertiesFormatException e) {
//                    e.printStackTrace();
//                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jComboBoxEspecieActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxEspecieActionPerformed
        ArrayList<Especie> especies = Controlador.getEspecies();
        for(Especie especie : especies){
            if(jComboBoxEspecie.getSelectedItem().equals(especie.getNombre())){
                if(especie.getTipoAlimentacion().esHerbivoro()){
                    jLabel6.setVisible(true);
                    jTextField4.setVisible(true);
                }else{
                    jLabel6.setVisible(false);
                    jTextField4.setVisible(false);
                }
            }
        }
        listarSectores();
        
    }//GEN-LAST:event_jComboBoxEspecieActionPerformed

