    public void listarEspecies(){
        ArrayList<Especie> especies = Controlador.getEspecies();
        for(Especie especie : especies){
            jComboBoxEspecie.addItem(especie.getNombre());
        }
    }
    public void listarSectores() {
        jComboBoxSector.removeAllItems();
        ArrayList<Especie> especies = Controlador.getEspecies();
        ArrayList<Sector> sectores = Controlador.getSectores();
        for (Especie especie : especies) {
            if (jComboBoxEspecie.getSelectedItem().equals(especie.getNombre()) && especie.getTipoAlimentacion().esCarnivoro()) {
                for (Sector sector : sectores) {
                    if (sector.getTipoAlimentacion().esCarnivoro()) {
                        String sectorNumber = String.valueOf(sector.getNumero());
                        jComboBoxSector.addItem(sectorNumber);
                    }
                }

            } else if (jComboBoxEspecie.getSelectedItem().equals(especie.getNombre()) && especie.getTipoAlimentacion().esHerbivoro()) {
                for (Sector sector : sectores) {
                    if (sector.getTipoAlimentacion().esHerbivoro()) {
                        String sectorNumber = String.valueOf(sector.getNumero());
                        jComboBoxSector.addItem(sectorNumber);
                    }
                }

            }
        }
    }
    public void listarPaises(){
        ArrayList<Pais> paises = Controlador.getPaises();
        jComboBoxPais.removeAllItems();
        for(Pais pais : paises){
            jComboBoxPais.addItem(pais.getNombre());
        }
    }
   
    
    
    

