    private void cargarComboEspecie(){
        DefaultComboBoxModel<Especie> modelo = new DefaultComboBoxModel<>();

        for (Especie e : Controlador.getEspecies()) {
            modelo.addElement(e);
        }

        especie.setModel(modelo);
    }
    
    private void cargarComboPais(){
        DefaultComboBoxModel<Pais> modelo = new DefaultComboBoxModel<>();

        for (Pais p : Controlador.getPaises()) {
            modelo.addElement(p);
        }

        pais.setModel(modelo);
    }

